      SUBROUTINE AMAT01(AIMQ01,RX,AL01)
      IMPLICIT REAL*8(A-H,O-Z)
      PARAMETER (NN01=7)
      DIMENSION RX(NN01)
      DIMENSION AL01(NN01,NN01,4)
      DO I=1,NN01
        DO J=1,NN01
          AL01(I,J,1)=0.0D0
          AL01(I,J,2)=0.0D0
          DO K=0,NN01 
            CALL WAV01(AIMQ01,K,RX(I),FUN1) 
            CALL WAV01(AIMQ01,K,RX(J),FUN2) 
            AL01(I,J,1)=AL01(I,J,1)+FUN1*FUN2
            AL01(I,J,2)=AL01(I,J,2)+FUN1*FUN2*DFLOAT(2*K)
          ENDDO
          AL01(I,J,3)=0.0D0
          DO K=0,NN01-1 
            CALL WAV01(AIMQ01,K,RX(I),FUN1) 
            CALL WAV01(AIMQ01,K+1,RX(J),FUN2) 
            AL01(I,J,3)=AL01(I,J,3)+FUN1*FUN2*SQRT(DFLOAT(K+1))
          ENDDO
          DO K=1,NN01
            CALL WAV01(AIMQ01,K,RX(I),FUN1) 
            CALL WAV01(AIMQ01,K-1,RX(J),FUN2) 
            AL01(I,J,3)=AL01(I,J,3)+FUN1*FUN2*SQRT(DFLOAT(K))
          ENDDO
          AL01(I,J,4)=0.0D0
          DO K=0,NN01-2 
            CALL WAV01(AIMQ01,K,RX(I),FUN1) 
            CALL WAV01(AIMQ01,K+2,RX(J),FUN2) 
            AL01(I,J,4)=AL01(I,J,4)+FUN1*FUN2*SQRT(DFLOAT((K+1)*(K+2)))
          ENDDO
          DO K=2,NN01
            CALL WAV01(AIMQ01,K,RX(I),FUN1) 
            CALL WAV01(AIMQ01,K-2,RX(J),FUN2) 
            AL01(I,J,4)=AL01(I,J,4)+FUN1*FUN2*SQRT(DFLOAT(K*(K-1)))
          ENDDO
        ENDDO
      ENDDO
      RETURN
      END
