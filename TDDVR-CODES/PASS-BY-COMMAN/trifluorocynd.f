      SUBROUTINE DQ01QD(Y1,Y2,Y3,Y4,Y5,Y6,Y7,X,H)
      IMPLICIT REAL*8(A-H,O-Z)
      PARAMETER (NN01=3,NN02=3,NN03=3,NN04=1)
      PARAMETER (NA05=3,NB05=3,NA06=3,NB06=3)
      PARAMETER (NA07=3,NB07=3,NA08=3,NB08=3)
      PARAMETER (NN1=NN01*NN02*NN03*NN04)
      PARAMETER (NN2=NA05*NB05*NA06*NB06*NA07*NB07*NA08*NB08)
      PARAMETER (NN=NN1*NN2)
      PARAMETER (MM=2*NN)
      DIMENSION Y1(MM),Y2(MM),Y3(MM),Y4(MM),Y5(MM),Y6(MM),Y7(MM)
      DIMENSION W11(MM),W12(MM),W13(MM),W14(MM)
      DIMENSION W21(MM),W22(MM),W23(MM),W24(MM)
      DIMENSION W31(MM),W32(MM),W33(MM),W34(MM)
      DIMENSION W41(MM),W42(MM),W43(MM),W44(MM)
      DIMENSION W51(MM),W52(MM),W53(MM),W54(MM)
      DIMENSION W61(MM),W62(MM),W63(MM),W64(MM)
      DIMENSION W71(MM),W72(MM),W73(MM),W74(MM)
      DIMENSION F(MM),F1(MM),F2(MM),F3(MM),F4(MM) 
      DIMENSION G(MM),G1(MM),G2(MM),G3(MM),G4(MM)
      DIMENSION P(MM),P1(MM),P2(MM),P3(MM),P4(MM)
      DIMENSION Q(MM),Q1(MM),Q2(MM),Q3(MM),Q4(MM)
      DIMENSION R(MM),R1(MM),R2(MM),R3(MM),R4(MM)
      DIMENSION S(MM),S1(MM),S2(MM),S3(MM),S4(MM)
      DIMENSION T(MM),T1(MM),T2(MM),T3(MM),T4(MM)
      N=MM
      H2=H*0.5E0
      X0=X
      H3=H/3.0E0
      H6=H/6.0E0
      H8=H/8.0E0
      CALL FEX(Y1,Y2,Y3,Y4,Y5,Y6,Y7,F,G,P,Q,R,S,T,N,X)
      DO 1 I=1,N
         W11(I)=Y1(I)+H3*F(I)
         W21(I)=Y2(I)+H3*G(I)
         W31(I)=Y3(I)+H3*P(I)
         W41(I)=Y4(I)+H3*Q(I)
         W51(I)=Y5(I)+H3*R(I)
         W61(I)=Y6(I)+H3*S(I)
         W71(I)=Y7(I)+H3*T(I)
   1  CONTINUE
