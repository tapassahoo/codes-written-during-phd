CCC
CCC   NORMALIZATION CHECK OF THE TRANSLATIONAL WAVEFUNCTION (GWP)
CCC
      IMPLICIT REAL*8(A-H,O-Z)
      COMPLEX*16 ZI,A1,XI,SS
      PARAMETER(NN=100)
      EKIN=1.0D0
      EMU=1.0D0
      ZI=DCMPLX(0.0D0,1.0D0)
      PI=4.0D0*DATAN(1.0D0)
      RMIN=-20.0D0
      RMAX=20.0D0
      DR=(RMAX-RMIN)/DFLOAT(NN)
      HBAR=0.06350781278D0
      AK_0=SQRT(2.0D0*EMU*EKIN)/HBAR
      R2_0=3.5D0
      R2_F=1.68D0
      SIGMA=0.21D0
      QQ=2.0D0*(R2_0-R2_F)/AK_0
      A1=1.0D0/(4.0D0*SIGMA*SIGMA-ZI*QQ)
      A2=8.0D0*SIGMA*SIGMA/(16.0D0*SIGMA**4+QQ*QQ)
      AN=(A2/PI)**0.25
      SS=DCMPLX(0.0D0,0.0D0)
      DO I=1,NN
      R2=RMIN+(I-1)*DR
      XI=AN*EXP(-ZI*AK_0*R2-A1*(R2-R2_0)**2)
      SS=SS+CONJG(XI)*XI*DR
      WRITE(11,44)R2,DBLE(XI),DIMAG(XI),ABS(XI)
      ENDDO
      WRITE(6,*)DBLE(SS),DIMAG(SS)
   44 FORMAT(1X,4f12.6)
      STOP
      END
                                         
