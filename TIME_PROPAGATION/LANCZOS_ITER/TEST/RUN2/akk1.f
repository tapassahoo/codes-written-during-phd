	IMPLICIT REAL*8(A-H,O-Z)
	PARAMETER (NN=200)
	DIMENSION AKI(NN),CKI(NN)
	DELTA=0.2D0
	PI=4.0D0*DATAN(1.0D0)
	AK0=20.0D0
	AKMIN=-80.0D0
	AKMAX=80.0D0
	DK=(AKMAX-AKMIN)/DFLOAT(NN)
	SUM=0.0D0
	DO I=1,NN
	  AKI(I)=AKMIN+(I-1)*DK
	  CKI(I)=DSQRT(2.0D0/PI)*DELTA
     1	        *EXP(-2.0D0*DELTA*DELTA*(AKI(I)-AK0)**2)
	  SUM=SUM+CKI(I)*DK
	  WRITE(6,'(1X,I8,2F12.6)')I,AKI(I),CKI(I)
	ENDDO
	WRITE(7,'(1X,F12.6)')SUM
	STOP
	END
