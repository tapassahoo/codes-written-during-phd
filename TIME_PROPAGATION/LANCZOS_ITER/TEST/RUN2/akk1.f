      SUBROUTINE DEFAT(DT,AT)
      IMPLICIT REAL*8 (A-H,O-Z)
      PARAMETER (NSTP=4096)
      DIMENSION AT(NSTP)
      PI=4.0D0*DATAN(1.0D0)
      PI2=PI*2.0D0
      ACONST=PI2/(NSTP*DT)
      NHALFT = NSTP/2+1
      DO I=1,NHALFT
        AT(I)=ACONST*(I-1)
      ENDDO
      DO I=NHALFT+1,NSTP
        AT(I)=ACONST*(I-1-NSTP)
      ENDDO
      RETURN
      END

