      WRITE(*,44)AJ_1,AM_1,AJ_2,AM_2,AJ,AM
      WRITE(*,*)
      WRITE(*,*)'CLGD=',CLGD
   44 FORMAT(1X,6f12.6)
      STOP
      END
      FUNCTION FACT(N) 
      IMPLICIT REAL*8(A-H,O-Z)
      IF(N.EQ.0) THEN
      VAL=1.0D0
      ELSE
      VAL=1.0D0
      DO I=1,N
      VAL=VAL*DFLOAT(I)
      ENDDO
      ENDIF
      FACT=VAL
      RETURN
      END
